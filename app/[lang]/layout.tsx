import type { Metadata } from 'next';
import { Geist, Geist_Mono } from 'next/font/google';
import '../globals.css';
import nextAuthOptions from '@Src/app/auth/config/next-auth';
import AuthProvider from '@Src/app/auth/providers/AuthProvider';
import NextAuthProvider from '@Src/app/auth/providers/NextAuthProvider';
import MockServerInit from '@Src/app/mock/inits/MockServerInit';
import ReactQueryProvider from '@Src/app/react-query/providers/ReactQueryProvider';
import StoreProvider from '@Src/app/store/providers/StoreProvider';
import { getServerSession } from 'next-auth';
import '@Src/shared/config/mock/mock-server.setup';
import I18nProvider from '@Src/app/i18n/providers/I18nProvider';
import { Locale } from '@Src/shared/config/i18n';

const geistSans = Geist({
  variable: '--font-geist-sans',
  subsets: ['latin'],
});

const geistMono = Geist_Mono({
  variable: '--font-geist-mono',
  subsets: ['latin'],
});

export const metadata: Metadata = {
  title: 'Create Next App',
  description: 'Generated by create next app',
};

export default async function RootLayout({
  children,
  params,
  interceptionModal,
}: Readonly<{
  children: React.ReactNode;
  // ts 컴파일 타임에는 lang 을 string 으로 받지 않으면 .next/types/validator.ts 에서 에러가 발생함
  params: Promise<{ lang: string }>;
  interceptionModal: React.ReactNode;
}>) {
  const session = await getServerSession(nextAuthOptions);
  const { lang } = await params;

  return (
    <html lang={lang}>
      <body className={`${geistSans.variable} ${geistMono.variable} antialiased`}>
        <StoreProvider>
          <ReactQueryProvider>
            <NextAuthProvider session={session}>
              <AuthProvider>
                <MockServerInit>
                  {/* 미들웨어에서 Locale 이외의 값을 거르기 때문에 as 로 변환 */}
                  <I18nProvider locale={lang as Locale}>
                    {children}
                    {interceptionModal}
                  </I18nProvider>
                </MockServerInit>
              </AuthProvider>
            </NextAuthProvider>
          </ReactQueryProvider>
        </StoreProvider>
        <div id="modal-root" />
      </body>
    </html>
  );
}
